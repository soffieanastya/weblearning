        <p class="text_content">
            Peristiwa tersebut terjadi juga pada listrik. Alirain air diibaratkan sebagai aliran listrik. Dinding yang menghalangi aliran air merupakan resistor. Sedangkan perbedaan ketinggian air disebut sebagai beda potensial.
        </p>
        <p class="text_content">
            “Semakin besar kayu menghambat aliran air, maka semakin kecil arus air.” Dapat berarti “semakin besar nilai hambatan pada resistor, maka semakin kecil arus listrik yang mengalir.” Sehingga, dapat disimpulkan bahwa nilai hambatan pada resistor berbanding terbalik dengan arus listrik. Jika hambatan kita beri symbol R, sedangkan arus listrik kita beri symbol i, maka secara matematis dapat ditulis sebagai berikut.
        </p>
        <b>R = 1/i</b>
        <br>
        <p class="text_content">
            “Semakin besar beda potensial atau semakin besar perbedaan ketinggian air, maka semakin cepat aliran arus air.” Dalam istilah ke-listrik-an, dapat ditulis “Semakin besar beda potensial, maka semakin cepat aliran arus listrik.” Dari pernyataan tersebut, dapat di simpulkan bahwa beda potensial (V) berbanding lurus dengan arus listrik (i). secara matematis, dapat ditulis sebagai berikut.
        </p>
        <b>V = i</b>
        <p class="text_content">
            Kedua persamaan tersebut dapat kita tulis menjadi: <br>
            
        </p>
        <b>V = i . R</b>
        <p class="text_content">
            Hal inilah yang dinamakan dengan hukum ohm. Teori dasar Hukum Ohm menyatakan bahwa “besar arus listrik yang mengalir melalui konduktor selalu berbanding lurus dengan tegangan yang dibebankan pada resistor tersebut”.
        </p>
        <p align="justify">
            <b>V = I . R</b> <br>
            Dengan:<br> 
            V = tegangan (V) <br>
            I = kuat arus listrik (A) <br>
            R = hambatan (ꭥ) <br><br>   
        </p>
        
        <p class="text_content">
            Selain memperhatikan nilai resistensi sebuah resistor, perlu juga memperhatikan nilai disipasi daya yang mampu didukung oleh resistor ketika mengalirkan arus listrik dengan rumus sebagai berikut.
        </p>

        <p align="justify">
            P = I <sup>2</sup> . R <br>
            Dengan: <br>
            P = daya listrik yang didukung resistor (W) <br>
            I = kuat arus listrik (A) <br>
            R = besar hambatan (ꭥ) <br><br>    
        </p>
        
