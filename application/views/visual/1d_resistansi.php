        <P class="text_content">
        	Resistansi Listrik (Electrical Resistance) adalah kemampuan suatu benda untuk <b>menghambat atau mencegah</b> aliran arus listrik. 
        </P>
        
        <p class="text_content">
          Berdasarkan hukum ohm, terdapat resistansi suatu penghantar pada rangkaian yang berpengaruh terhadap kuat arus yang melalui penghantar tersebut. Untuk mengatur kuat arus yang melalui penghantar dapat dilakukan dengan mengubah besar kecil nilai resistansinya. Berdasarkan resistansi suatu bahan dapat digolongkan menjadi dua kelompok besar, yaitu konduktor (penghantar) dan isolator (tidak dapat menghantarkan). Benda yang dapat menghasilkan resistansi disebut resistor. Dalam SI <b>satuan</b> resistansi adalah <b>ohm</b>. 
        </p>

        <P class="text_content">
          Hubungan antara resistansi atau hambatan listrik dengan tegangan dan arus listrik dapat dijelaskan dengan hokum Ohm yang dikemukakan oleh seorang fisikawan Jerman yang bernama Georg Simon Ohm (1789-1854) pada tahun 1825. Persamaan hokum ohm adalah
        </P>
        <br>
        
        <div  style="text-align: center;">
          <b>V = I . R</b> <br>
        </div>

        <p align="justify">
          Di mana: <br>
          V = tegangan listrik (voltage), yang memiliki satuan Volt <br>
          I = arus listrik (current), yang memiliki satuan Ampere <br>
          R = hambatan listrik atau resistansi (resistance), yang memiliki satuan Ohm <br>
        </p>
        
        <br>
