        <p align="justify"><font size="6">Apa itu muatan negatif dan atom?</font></p>
        <img src="<?php echo base_url('assets/img/kelistrikan/atmm.png') ?>" width="60%">
        <p class="text_content">
            Atom merupakan <b>bagian terkecil dari suatu benda</b> (dimana bagian terkecil dari suatu benda tersebut dapat kita sebut partikel) yang terdiri daripertikel-pertikel bermuatan positif yang disebut  <b>proton</b> , partikel bermuatan negatif yang disebut <b>elektron</b> dan partikel tidak bermuatan yang disebut <b>neutron</b> .
        </P>
        <p class="text_content">
            Pergerakan antara rambut dan penggaris yang memiliki muatan listrik secara beraturan akan menimbulkan muatan listrik yang disebut energi listrik. 
        </p>
        <p align="justify">
            <font size="6">Apa itu muatan listrik?</font>
        </p>
        <p class="text_content">
            Muatan listrik merupakan materi/bahan suatu, benda yang membuatnya mengalami gaya pada benda lain yang berdekatan dan memiliki muatan listrik. Benyamin franklin menamakan kedua jenis aliran listrik dengan listrik bolak-balikrik bermuatan positif (ketika benda memiliki elektron lebih sedikit daripada proton) serta listrik bermuatan negatif (ketika benda memiliki elektron lebih banyak daripada proton).
        </p>
        <p align="justify">
            <font size="6">Apa itu energi listrik?</font>
        </p>
        <p class="text_content">
            Energi listrik merupakan energi yang bersumber dari muatan listrik yang membentuk pola medan listrik statis dan mengalirkan elektron-elektron dalam jumlah tertentu melalui konduktor.
        </p>
